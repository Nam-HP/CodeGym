# Bước 1 : Lấy công cụ vẽ
import turtle

# Bước 2 : Lấy viết
t= turtle.Turtle()

# Bước 3 Tiến tới 100 đơn vị
t.forward(100)
# quay phải 90 độ
t.right(90)
# Tiến tới 100 đơn vị
t.forward(100)
# rẽ phải 
t.right(90)
# Tiến tới 100 đơn vị
t.forward(100)
# tiếp tục quay phải 90 độ
t.right(90)
# Tiến tới 100 đơn vị
t.forward(100)


turtle.done()